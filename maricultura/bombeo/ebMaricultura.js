const wss = require('../../../index');
const net = require('net');
const readline = require('readline');
const { arregloTagsMariculturaEB, arregloTagsMariculturaEBGeneral, arregloTagsMariculturaEBTCP, arregloTagsMariculturaEBAntenas } = require('../../../helpers/maricultura/arregloTagsBombeo');

let interval = null; 
let dataCache = {
    EBMaricultura: [],
    EBMariculturaGral: [],
    EBMariculturaTCP: [],
    EBMariculturaAntenas: []
}; 

let completedFuntions = 0
const clientSubscriptions = new Map()

const checkAllFuntionsComplete = () => {
    if (completedFuntions === 4) {
        broadcastData()
        completedFuntions = 0
    }
}

const broadcastData = () => {
    clientSubscriptions.forEach((isSubscribed, client) => {
        if (isSubscribed) {
            //console.log("Sending data: ", JSON.stringify(dataCache))
            client.send(JSON.stringify(dataCache));
        }
    })

}

const getEBMaricultura = () => {
    try {
        let client = net.connect('\\\\.\\pipe\\HmiRuntime', () => {
            let tagReadCommand = `{"Message":"ReadTag","Params":{"Tags": ${arregloTagsMariculturaEB()}},"ClientCookie":"myReadTagRequest1"}\n`;
            let obje = {};
            client.write(tagReadCommand);

            const rl = readline.createInterface({
                input: client,
                crlfDelay: Infinity
            });
            rl.on('line', (line) => {
                const obj = JSON.parse(line);
                if (obj.Message === 'NotifyReadTag') {
                    const arreglo = obj.Params.Tags;
                    dataCache.EBMaricultura = [];
                    arreglo.forEach((ex, i) => {
                        obje[ex.Name.slice(23)] = ex.Value;     
                    });
                    dataCache.EBMaricultura.push(obje)
                    obje = {}
                    completedFuntions++
                    checkAllFuntionsComplete()
                }
                client.end();
            });
        });
    } catch (error) {
        console.error(error);
        res.status(500).json({ error: 'Internal Server Error' });
    }
};


